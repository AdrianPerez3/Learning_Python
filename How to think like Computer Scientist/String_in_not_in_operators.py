'''The in and not in operators'''

# The in operator tests if one string is a substring of another:
# Result will be boolean

print('p' in 'apple')
print('i' in 'apple')
print('ap' in 'apple')
print('pa' in 'apple')

print('a' in 'a')
print('apple' in 'apple')
print('' in 'a')
print('' in 'apple')

print('x' not in 'apple')
