'''
Operators and Operands
'''
# The result of two integer division is float number
minutes = 645
hours = minutes / 60
print(hours)

# But if we use the integer division which is // sigh below it will result integer.
print(7 / 4)
print(7 // 4)
minutes = 645
hours = minutes // 60
print(hours)

# Remainder returns the remainder from the division.
quotient = 7 // 3     # This is the integer division operator
print(quotient)
remainder = 7 % 3
print(remainder)
